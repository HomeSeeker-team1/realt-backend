const regx = {
  name: () => /^([А-ЯЁ]{1}[а-яё]{1,23}|[A-Z]{1}[a-z]{1,23})$/,
  patronymic: () => /^([А-ЯЁ]{0}[а-яё]{0,23}|[A-Z]{1}[a-z]{1,23})$/,
  password: () => /(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])[0-9a-zA-Z!@#$%^&*]{6,}/,
  phone: () => /^[0-9]{6,50}$/,
  text: () => /([а-яёА-ЯЁ]{1,23}|[a-zA-Z]{1,23})/,
};

export default regx;
